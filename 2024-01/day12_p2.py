from pprint import pp  # noqa


Position = tuple[int, int]
Garden = dict[Position, str]
Region = set[Position]
ScanLine = list[tuple[Position, Position]]

FIRST_IN = "V"
SECOND_IN = "^"
NO_EDGE = "."


def parse(raw):
    out = {}
    for row, line in enumerate(raw.split("\n")):
        for col, char in enumerate(line):
            out[col, row] = char
    return out


def get_region(garden, pos: Position) -> tuple[Region, int, str]:
    perim = 0
    region: Region = set()

    plant = garden[pos]
    processed: set[Position] = set()
    queue: set[Position] = {pos}

    while len(queue) > 0:
        pos = queue.pop()
        region.add(pos)
        x, y = pos
        coords = [(x - 1, y), (x + 1, y), (x, y - 1), (x, y + 1)]

        for coord in coords:
            if coord in processed:
                continue
            if garden.get(coord) == plant:
                queue.add(coord)
            else:
                perim += 1

        processed.add(pos)

    return region, perim, plant


def get_region_bounds(region: Region) -> tuple[int, int, int, int]:
    pos = next(iter(region))
    minx, miny = pos
    maxx, maxy = pos

    for x, y in region:
        minx = min(x, minx)
        miny = min(y, miny)
        maxx = max(x, maxx)
        maxy = max(y, maxy)

    return minx, miny, maxx, maxy


def count_segments_in_str(edge_str: str) -> int:
    total = 0
    for seg in edge_str.split(NO_EDGE):
        spl1 = [s for s in seg.split(FIRST_IN) if s]
        spl2 = [s for s in seg.split(SECOND_IN) if s]
        sub_segs = len(spl1) + len(spl2)
        total += sub_segs
    return total


def scan_line_to_string(region: Region, line: ScanLine) -> str:
    edge_str = ""
    for p1, p2 in line:
        in1 = p1 in region
        in2 = p2 in region
        if in1 == in2:
            edge_type = NO_EDGE
        else:
            edge_type = FIRST_IN if in1 else SECOND_IN
        edge_str += edge_type
    return edge_str


def get_scan_lines(region: Region) -> list[ScanLine]:
    minx, miny, maxx, maxy = get_region_bounds(region)
    lines = []

    for y in range(miny - 1, maxy + 1):
        line = []
        for x in range(minx, maxx + 1):
            top, bottom = (x, y), (x, y + 1)
            line.append((top, bottom))
        lines.append(line)

    for x in range(minx - 1, maxx + 1):
        line = []
        for y in range(miny, maxy + 1):
            left, right = (x, y), (x + 1, y)
            line.append((left, right))
        lines.append(line)

    return lines


def count_sides(region: Region) -> int:
    n_sides = 0
    for line in get_scan_lines(region):
        edge_str = scan_line_to_string(region, line)
        n_sides += count_segments_in_str(edge_str)
    return n_sides


def main(puzzle_input):
    garden: dict[Position, str] = parse(puzzle_input)
    processed: set[Position] = set()
    total = 0

    for pos in garden:
        if pos in processed:
            continue
        region, _perim, plant = get_region(garden, pos)
        processed.update(region)
        n_sides = count_sides(region)
        print(plant, n_sides)
        total += len(region) * n_sides

    print(total)


