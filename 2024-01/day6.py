from pprint import pp


def parse(raw):
	out = {}
	guard_pos = None
	for row,line in enumerate(raw.split("\n")):
		for col,char in enumerate(line):
			out[col,row] = char
			if char in "^V><":
				guard_pos = (col, row)
	return out, guard_pos, col, row


def update(state, guard_pos):
	state = state.copy()
	guard_dir = state[guard_pos]

	x, y = guard_pos
	if guard_dir == "V":
		new_pos = x, y+1
		new_dir = "<"
	elif guard_dir == "^":
		new_pos = x, y-1
		new_dir = ">"
	elif guard_dir == "<":
		new_pos = x-1, y
		new_dir = "^"
	elif guard_dir == ">":
		new_pos = x+1, y
		new_dir = "V"
	
	char = state.get(new_pos)
	if char == "#":
		state[guard_pos] = new_dir
		return state, guard_pos

	state[guard_pos] = "X"
	if char is None:
		return state, None

	state[new_pos] = guard_dir
	return state, new_pos
	


def print_map(state, w, h):
	for y in range(h):
		for x in range(w):
			char = state[x,y]
			print(char, end="")
		print()

		

def main(puzzle_input):
	state, guard_pos, width, height = parse(puzzle_input)
	while guard_pos is not None:
		print_map(state, width, height)
		input("Press enter to continue")
		state, guard_pos = update(state, guard_pos)
	result = len( [c for c in state.values() if c == "X"] )
	print(result)

	

